function premiumPopOpen(){
    document.getElementById("popupWindow").style.display="block";
    document.getElementById("premiumPopUpBackground").style.display = "block";
}
function premiumPopClose(){
    document.getElementById('popupWindow').style.display="none";
    document.getElementById("premiumPopUpBackground").style.display = "none";
}
function playlistOpen(){
    document.getElementById("popUpWindowPlaylist").style.display="block";
}
function playlistClose(){
    document.getElementById("popUpWindowPlaylist").style.display="none";
}

